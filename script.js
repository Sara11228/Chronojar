let mesActual = new Date().getMonth();

function generarCalendario(mes) {
  diasContainer.innerHTML = "";
  mesNombre.textContent = obtenerNombreMes(mes);

  const diasEnMes = new Date(2025, mes + 1, 0).getDate();
  const primerDiaSemana = new Date(2025, mes, 1).getDay();
  const offset = primerDiaSemana === 0 ? 6 : primerDiaSemana - 1;

  for (let i = 0; i < offset; i++) {
    const empty = document.createElement("div");
    empty.className = "dia vacío";
    diasContainer.appendChild(empty);
  }

  for (let dia = 1; dia <= diasEnMes; dia++) {
    const divDia = document.createElement("div");
    divDia.className = "dia";
    divDia.textContent = dia;
    divDia.onclick = () => mostrarPopup(dia, mesActual);
    diasContainer.appendChild(divDia);
  }
}

generarCalendario(mesActual);

function obtenerNombreMes(mes) {
  const nombres = [
    "Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio",
    "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"
  ];
  return nombres[mes];
}

function cambiarMes(cambio) {
  mesActual += cambio;
  if (mesActual < 0) mesActual = 11;
  if (mesActual > 11) mesActual = 0;
  generarCalendario(mesActual);
}

